    override func tableView(tableView: UITableView, cellForRowAtIndexPath indexPath: NSIndexPath) -> UITableViewCell {
        let cell = tableView.dequeueReusableCellWithIdentifier("cell", forIndexPath: indexPath) as UITableViewCell

        // Configure the cell...
        cell.textLabel?.numberOfLines = 0
        switch indexPath.section {
        case 0:
            cell.textLabel?.text = movieDetail?.title
            return cell
        case 1:
            cell.textLabel?.text = movieDetail?.location
            return cell
        case 2:
            var showTimeString = ""
            for show in movieDetail!.showTimeList {
                var string = show.objectForKey("time") as NSString
                showTimeString += "\(string)\n"
            }
            cell.textLabel?.text = showTimeString
            return cell
        case 3:
            cell.textLabel?.text = movieDetail?.name
            return cell
        case 4:
            cell.textLabel?.text = movieDetail?.descriptionFilterHtml
            return cell
        default:
            break
        }

        return cell
    }
    
    override func tableView(tableView: UITableView, titleForHeaderInSection section: Int) -> String? {
        switch section {
        case 0:
            return "電影名稱"
        case 1:
            return "地址"
        case 2:
            return "播放場次"
        case 3:
            return "地點"
        case 4:
            return "其它資訊"
        default:
            return ""
        }
    }

